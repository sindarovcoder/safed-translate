function extractLanguages(text) {
    if (!text) return null;
    const cleanText = text.replace(/^\./, "").trim(); // Boshidagi nuqtani olib tashlaymiz
    let [to, from] = cleanText.split(" ");
    if (!googleTranslateLanguages[to]) return null
    from = from || 'auto'; // Agar from bo'sh bo'lsa, 'auto'ga o'zgartiramiz
    return { to, from };
}

